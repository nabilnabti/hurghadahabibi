// Helper functions
export function getActivityBySlug(slug: string): Activity | undefined {
  return activities.find((a) => a.slug === slug);
}

export function getActivitiesByCategory(category: Activity["category"]): Activity[] {
  return activities.filter((a) => a.category === category);
}

export function getActivitiesByDestination(destination: Activity["destination"]): Activity[] {
  return activities.filter((a) => a.destination === destination);
}

export function getFeaturedActivities(): Activity[] {
  return activities.filter((a) => a.featured);
}

export function searchActivities(filters: {
  destination?: string;
  category?: string;
  minPrice?: number;
  maxPrice?: number;
  query?: string;
}): Activity[] {
  return activities.filter((a) => {
    if (filters.destination && a.destination !== filters.destination) return false;
    if (filters.category && a.category !== filters.category) return false;
    if (filters.minPrice && a.price < filters.minPrice) return false;
    if (filters.maxPrice && a.price > filters.maxPrice) return false;
    if (filters.query) {
      const q = filters.query.toLowerCase();
      return (
        a.title.toLowerCase().includes(q) ||
        a.description.toLowerCase().includes(q) ||
        a.location.toLowerCase().includes(q)
      );
    }
    return true;
  });
}
